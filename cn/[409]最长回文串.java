//给定一个包含大写字母和小写字母的字符串，找到通过这些字母构造成的最长的回文串。 
//
// 在构造过程中，请注意区分大小写。比如 "Aa" 不能当做一个回文字符串。 
//
// 注意: 
//假设字符串的长度不会超过 1010。 
//
// 示例 1: 
//
// 
//输入:
//"abccccdd"
//
//输出:
//7
//
//解释:
//我们可以构造的最长的回文串是"dccaccd", 它的长度是 7。
// 
// Related Topics 哈希表


// 思路：动态规划求解
// 注意事项：总是先得到小子串的回文判定，然后大子串才能参考小子串的判断结果，即填表顺序很重要。
// 也就是说 在状态转移方程中，我们是从长度较短的字符串   向长度较长的字符串进行转移的，因此一定要注意动态规划的循环顺序。

// 注意：在状态转移方程中，我们是从长度较短的字符串   向长度较长的字符串进行转移的，因此一定要注意动态规划的循环顺序。
// 外层循环 j=1 控制了 先从短字符串判断是否位回文串

